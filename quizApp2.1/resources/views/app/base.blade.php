    // Función para mostrar u ocultar el contenido según el valor de la sesión 'nombre'
    function toggleContentVisibility() {
        var sessionNombre = "{{ session('nombre') }}";
        var contentDiv = document.getElementById('content');
        var label = document.getElementById('alias');

        // Verificar si la sesión 'nombre' tiene un valor
        if (sessionNombre.trim() !== '') {
            // Mostrar el contenido
            contentDiv.style.display = 'block';
            label.textContent = 'Bienvenido, ' + sessionNombre;
        } else {
            // Ocultar el contenido si la sesión 'nombre' está vacía
            contentDiv.style.display = 'none';
            label.textContent = 'Introduce un alias';

        }
    }

    // Llamar a la función inicialmente para establecer el estado inicial
